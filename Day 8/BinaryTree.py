# Binary Tree

# Types
# - Full Binary Tree     ----> Each node has either 0 or 2 children, No node has a single child 
# - Complete Binary Tree ----> All levels except possibly the last are completely filled, Nodes in the last level are filled from left to right
# - Perfect Binary Tree  ----> All internal nodes have exactly two nodes, All leaf nodes are at the same level
# - Balanced Binary Tree ----> 

# Operation in Binary Tree
# 1) Creation of Tree 
# 2) Insertion of a  node 
# 3) Deletion of a node 
# 4) Search for a value 
# 5) Traverse all nodes 
# 6) Deletion of tree

# Traversal of BST
# 1) Depth first Search
# - Preorder   -------> Root-->Left-->Right
# - Inorder    -------> Left-->Root-->Right
# - Post Order -------> Left-->Right-->Root

# 2) Breadth First Seaarch
# - Level Order Traversal

